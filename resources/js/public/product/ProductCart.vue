<template>
    <div>
        <loader v-if="isLoading"></loader>
        <div class="row justify-content-center" v-if="!isLoading">
            <div class="card__product my-3"
                 v-for="product in products"
                 v-if="product.published === 1"
            >
                <a class="text-decoration-none"
                   v-bind:href="host + '/product/' + product.id"
                >

                    <div class="card__image">
                        <img :src="'/storage/products/350/' + product.preview" :alt="product.h1">
                    </div>

                    <div class="card__body">
                        <h5 class="card__label">{{ product.h1 }}</h5>
                        <div class="card__price">
                            <div v-if="product.discount_price === null"
                                 class="card__price-without-discount">{{ product.price }} грн.
                            </div>

                            <div v-if="product.discount_price > null">
                                <div class="card__old-price">{{ product.price }} грн.</div>
                                <div class="card__actual-price">{{ product.discount_price }} грн.</div>
                            </div>


                        </div>
                        <span class="card__button">Подробнее</span>
                    </div>
                </a>
            </div>
        </div>
        <div class="row">
            <div class="col">
                <paginate
                    :page-count="pageCount"
                    :page-range="3"
                    :margin-pages="2"
                    :click-handler="fetch"
                    :prev-text="'<'"
                    :next-text="'>'"
                    :container-class="'pagination justify-content-center'"
                    :page-link-class="'page-link'"
                    :prev-link-class="'page-link'"
                    :next-link-class="'page-link'"
                    :page-class="'page-item'">
                </paginate>
            </div>
        </div>
    </div>
</template>

<script>
export default {
    data() {
        return {
            host: window.location.origin,
            isLoading: true,
            products: [],
            pageCount: 1,
            showingFrom: 1,
            showingTo: 1,
            total: 1,
            endpoint: '/api/v1/product?page='
        }
    },
    mounted() {
        axios.get('/api/v1/product')
            .then(({data}) => this.deleteCartListSuccessResponse(data))
            .catch((response) => this.deleteCartListErrorResponse(response));
    },
    methods: {
        deleteCartListSuccessResponse(data) {
            this.isLoading = false;
            this.products = data.result.data;
            this.total = data.result.total;
            this.showingFrom = data.result.from;
            this.showingTo = data.result.to;
            this.pageCount = data.result.last_page;
        },
        deleteCartListErrorResponse(response) {
            console.log(response);
        },
        fetch(page = 1) {
            this.isLoading = true;
            axios.get(this.endpoint + page)
                .then(({data}) => {
                    this.isLoading = false;
                    this.products = data.result.data;
                    this.total = data.result.total;
                    this.showingFrom = data.result.from;
                    this.showingTo = data.result.to;
                    this.pageCount = data.result.last_page;
                });
        },
    }
}
</script>
